    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String idjenis, tipe;
        int hasil = 0;
        idjenis = txtIDJenis.getText();
        tipe = txtJenis.getText();
        
        int quest = JOptionPane.showConfirmDialog(this, "Yakin Ingin Menghapus Data " + tipe + "?", "Info", JOptionPane.YES_NO_OPTION);
        
        if (quest == JOptionPane.YES_OPTION) {
            if (idjenis.equals("")) {
                JOptionPane.showMessageDialog(this, "Data Tidak Lengkap, Silahkan Cek Kembali!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            } else {
                ModelJenisKendaraan jk = new ModelJenisKendaraan();
                jk.setId_jenis(idjenis);

                conJenis = new ControllerJenisKendaraan();
                hasil = conJenis.hapusData(jk);

                if (hasil == 1) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                } else {
                    JOptionPane.showMessageDialog(this, "Data Gagal Dihapus!");
                }

                loadData(0);
                enableButton("Null");
                enableForm(false);
                clearData();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

