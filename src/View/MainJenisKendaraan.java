    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String idjenis, tipe, harga;
        int hasil = 0;
        idjenis = txtIDJenis.getText();
        tipe = txtJenis.getText();
        harga = txtHarga.getText();
        
        if (idjenis.equals("") || tipe.equals("") || harga.equals("")) {
            JOptionPane.showMessageDialog(this, "Data Tidak Lengkap, Silahkan Cek Kembali!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else {
            ModelJenisKendaraan jk = new ModelJenisKendaraan(idjenis, tipe, Integer.parseInt(harga));

            conJenis = new ControllerJenisKendaraan();
            hasil = conJenis.simpanData(jk);

            if (hasil == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Disimpan!");
            }

            clearData();
            loadData(0);
            enableButton("Null");
            enableForm(false);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

