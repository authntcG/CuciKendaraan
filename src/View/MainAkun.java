    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String idakun, nama, alamat, notel, username, password, akses;
        int hasil = 0;
        idakun = txtID.getText();
        nama = txtNama.getText();
        alamat = txtAlamat.getText();
        notel = txtNoTel.getText();
        username = txtUsername.getText();
        password = txtPassword.getText();
        akses = (String) cbbAkses.getSelectedItem();
        
        if (idakun.equals("") || nama.equals("") || alamat.equals("") || notel.equals("") || username.equals("") || password.equals("") || akses.equals("Pilih Hak Akses")) {
            JOptionPane.showMessageDialog(this, "Data Tidak Lengkap, Silahkan Cek Kembali!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else {
            ModelAkun ma = new ModelAkun(idakun, nama, alamat, username, password, notel, ABORT);

            if (akses.equals("Administrator")) {
                ma.setAkses(1);
            } else {
                ma.setAkses(2);
            }

            conAkun = new ControllerAkun();
            hasil = conAkun.simpanData(ma);

            switch (hasil) {
                case 1:
                    JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
                    loadData(0);
                    clearData();
                    enableForm(false);
                    enableButton("Null");
                    break;
                case -1:
                    JOptionPane.showMessageDialog(this, "Username Telah Digunakan!");
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Data Gagal Disimpan!");
                    break;
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

