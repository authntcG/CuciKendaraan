    private void loadData(int param){
        List<ModelAkun> lsAkun = new ArrayList<ModelAkun>();
        conAkun = new ControllerAkun();
        
        if (param == 1) {
            String data = txtCari.getText();
            ModelAkun ma = new ModelAkun();
            ma.setNama(data);
            lsAkun = conAkun.caridataAkun(ma);
        } else {
            lsAkun = conAkun.ambilAkun(0);
        }
        
        Object [][] obAkun =    new Object[lsAkun.size()][7];
        
        int i = 0;
        
        for (ModelAkun ma : lsAkun) {
            obAkun[i][0] = ma.getIdakun();
            obAkun[i][1] = ma.getNama();
            obAkun[i][2] = ma.getAlamat();
            obAkun[i][3] = ma.getNotel();
            obAkun[i][4] = ma.getUsername();
            obAkun[i][5] = ma.getPassword();
            if (ma.getAkses() == 1) {
                obAkun[i][6] = "Administrator";
            } else {
                obAkun[i][6] = "Pengguna";
            }
            i++;
        }
        
        tblAkun.setModel(new javax.swing.table.DefaultTableModel(
            obAkun,
            new String [] {
                "ID Akun", "Nama Pengguna", "Alamat", "No. Telepon", "Username", "Password", "Level Akses"
            }
        ));
        jScrollPane1.setViewportView(tblAkun);
        tblAkun.setDefaultEditor(Object.class, null);
        tblAkun.getColumnModel().getColumn(5).setMinWidth(0);
        tblAkun.getColumnModel().getColumn(5).setMaxWidth(0);
        //pack();
    }
    
